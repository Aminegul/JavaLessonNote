package day07loops;

public class C08ForLoops {

    public static void main(String[] args) {

        for (int i=1; i<=6; i++){

            System.out.println(i);

        }

        for(int j=0; j<=10; j=j+3){

            System.out.println(j+" ");
        }

        int y=1;
         for(int s=0; s<4; s++){

             y+=s;  //y=y+s
         }

        System.out.println(y);


    }

}
